(async () => {
  const mongoUri = process.env.MONGODB_URI;
  if (!mongoUri) {
    console.error("❌ Missing MONGODB_URI. Add it to server/.env");
    process.exit(1);
  }

  try {
    await mongoose.connect(mongoUri);
    console.log("✅ Connected to MongoDB");

    let created = 0;
    let skipped = 0;

    for (const [i, data] of products.entries()) {
      const existing = await Product.findOne({ name: data.name });
      if (existing) {
        console.log(`⏭  Skipping (already exists): ${data.name}`);
        skipped++;
        continue;
      }

      const product = new Product({
        ...data,
        displayOrder: i + 1,
      });

      await product.save();
      console.log(`✅ [${i + 1}/${products.length}] Created: ${data.name}`);
      created++;
    }

    console.log(`\n🎉 Seeding complete! ${created} created, ${skipped} skipped.`);
  } catch (err) {
    console.error("❌ Seeding failed:", err.message);
  } finally {
    await mongoose.disconnect();
    console.log("🔌 Disconnected from MongoDB");
  }
})();
